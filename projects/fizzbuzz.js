var output = [];
var count = 1;

function fizzbuzz() {
  if (count % 3 === 0 && count % 5 === 0) {
    output.push("FizzBuzz");
  } else if (count % 3 === 0) {
    output.push("Fizz");
  } else if (count % 5 === 0) {
    output.push("Buzz");
  } else {
    output.push(count);
  }
  
  count++;
  console.log(output);
}

// Call the fizzbuzz function multiple times to see the sequence.
var n= prompt("enter the value of n");
for (var i = 0; i < n; i++) {
  fizzbuzz();
}
